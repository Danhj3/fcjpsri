const languageButtons = document.querySelectorAll('.language-switcher__btn');
const navToggle = document.querySelector('.site-nav__toggle');
const navMenu = document.querySelector('#main-menu');
const srToggle = document.querySelector('.site-nav__toggle .sr-only');
const footerRightsEl = document.querySelector('[data-i18n="footer.rights"]');
const counterElements = document.querySelectorAll('[data-counter]');
const form = document.querySelector('#admissions-form');
const feedback = document.querySelector('.form-card__feedback');

function setMeta({ title, description }) {
    document.title = title;
    const metaDescription = document.querySelector('meta[name="description"]');
    if (metaDescription) {
        metaDescription.setAttribute('content', description);
    }
}

function updatePlaceholders(lang) {
    document.querySelectorAll('[data-i18n-placeholder]').forEach((element) => {
        const key = element.getAttribute('data-i18n-placeholder');
        const translation = key.split('.').reduce((acc, part) => acc?.[part], translations[lang]);
        if (translation) {
            element.setAttribute('placeholder', translation);
        }
    });
}

function updateAriaLabels(lang) {
    document.querySelectorAll('[data-i18n-aria-label]').forEach((element) => {
        const key = element.getAttribute('data-i18n-aria-label');
        const translation = key.split('.').reduce((acc, part) => acc?.[part], translations[lang]);
        if (translation) {
            element.setAttribute('aria-label', translation);
        }
    });
}

function updateText(lang) {
    document.querySelectorAll('[data-i18n]').forEach((element) => {
        const key = element.getAttribute('data-i18n');
        const translation = key.split('.').reduce((acc, part) => acc?.[part], translations[lang]);
        if (!translation) return;

        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
            element.value = translation;
        } else if (element.tagName === 'TIME') {
            element.dateTime = translation;
            element.textContent = translation;
        } else {
            element.textContent = translation;
        }
    });
}

function updateFooterYear(lang) {
    if (!footerRightsEl) return;
    const template = translations[lang].footer.rights;
    const year = new Date().getFullYear();
    footerRightsEl.innerHTML = template.replace('{year}', `<span id="current-year">${year}</span>`);
}

function setLanguage(lang) {
    const locale = translations[lang] ? lang : 'es';
    document.documentElement.lang = locale;
    languageButtons.forEach((button) => {
        const isActive = button.dataset.language === locale;
        button.classList.toggle('is-active', isActive);
        button.setAttribute('aria-pressed', String(isActive));
    });

    const { meta } = translations[locale];
    setMeta(meta);
    updateText(locale);
    updatePlaceholders(locale);
    updateAriaLabels(locale);
    updateFooterYear(locale);
    localStorage.setItem('fcjpsri-lang', locale);
}

languageButtons.forEach((button) => {
    button.addEventListener('click', () => {
        const newLang = button.dataset.language;
        setLanguage(newLang);
    });
});

if (navToggle && navMenu) {
    navToggle.addEventListener('click', () => {
        const isExpanded = navToggle.getAttribute('aria-expanded') === 'true';
        navToggle.setAttribute('aria-expanded', String(!isExpanded));
        navMenu.classList.toggle('is-open', !isExpanded);
        if (srToggle) {
            srToggle.textContent = !isExpanded
                ? translations[document.documentElement.lang].nav.toggleClose
                : translations[document.documentElement.lang].nav.toggleOpen;
        }
    });
}

function animateCounters(entries) {
    entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        const element = entry.target;
        const endValue = Number(element.dataset.counter);
        const duration = 1400;
        const start = performance.now();

        function update(now) {
            const progress = Math.min((now - start) / duration, 1);
            const eased = progress < 0.5 ? 4 * progress * progress * progress : 1 - Math.pow(-2 * progress + 2, 3) / 2;
            element.textContent = Math.floor(endValue * eased).toLocaleString(document.documentElement.lang);
            if (progress < 1) {
                requestAnimationFrame(update);
            } else {
                element.textContent = endValue.toLocaleString(document.documentElement.lang);
            }
        }

        requestAnimationFrame(update);
        observer.unobserve(element);
    });
}

const observer = new IntersectionObserver(animateCounters, {
    threshold: 0.6
});

counterElements.forEach((element) => observer.observe(element));

if (form && feedback) {
    form.addEventListener('submit', (event) => {
        event.preventDefault();
        const formData = new FormData(form);
        const isValid = form.checkValidity();
        const lang = document.documentElement.lang;
        if (!isValid) {
            feedback.textContent = translations[lang].admissions.form.error;
            feedback.style.color = '#b62333';
            return;
        }

        const name = formData.get('name');
        const program = formData.get('program');
        const email = formData.get('email');

        if (!name || !program || !email) {
            feedback.textContent = translations[lang].admissions.form.error;
            feedback.style.color = '#b62333';
            return;
        }

        form.reset();
        feedback.textContent = translations[lang].admissions.form.success;
        feedback.style.color = '#1f7a4d';
    });
}

const savedLanguage = localStorage.getItem('fcjpsri-lang');
setLanguage(savedLanguage || document.documentElement.lang || 'es');
